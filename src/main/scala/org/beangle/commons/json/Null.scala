package org.beangle.commons.json

object Null {

  override def equals(obj: Any): Boolean = {
    obj match {
      case null => true
      case o: AnyRef => o eq this
      case a: Any => false
    }
  }

  override def hashCode: Int = 0

  override def toString: String = "null"
}
